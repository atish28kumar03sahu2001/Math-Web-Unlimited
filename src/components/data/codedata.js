export const DivisionConcept = 
`Dividend = (Quotient * Divisor) + Remainder
Example: (21 * 7) + 3 = 150

Divisor = (Dividend - Remainder) / Quotient
Example: (150 - 3) / 21 = 7

Remainder = Dividend - (Quotient * Divisor)
Example: 150 - (21 * 7) = 3

Quotient = (Dividend - Remainder) / Divisor
Example: (150 - 3) / 7 = 21`;

